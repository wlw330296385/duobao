var tipAlert = {
    init: function() {
        var _html = '<div style="position:fixed; bottom:10%; width:100%; text-align:center; display:none; z-index:999;" id="web_tip_alert"><p style="color:#fff; background-color:rgba(0, 0, 0, 0.8); font-size:14px; display:inline-block; padding:7px 13px; border-radius:3px;" id="web_tip_alert_content">等待幸运大奖中...</p></div>';
        $('body').append(_html);
    },
    show: function() {
        $('#web_tip_alert').show();
    },
    show_with_content: function(content) {
        $('#web_tip_alert_content').text(content);
        $('#web_tip_alert').show();
    },
    hide: function() {
        $('#web_tip_alert').hide();
    }
}
